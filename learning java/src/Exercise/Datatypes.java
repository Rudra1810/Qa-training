package Exercise;

public class Datatypes {

	public static void main(String[] args) {
	    int a=10;
	    System.out.println("datatype of a is integer and value is "+a);
	    byte b=127;
	    System.out.println("datatype of b is byte and value is "+b);
	    short s=32767;
	    System.out.println("datatype of s is short and value is "+s);
	    long l=6546431942641493L;
	    System.out.println("datatype of l is long and value is "+l);
	    float f=3.14F;
	    System.out.println("datatype of f is float and value is "+f);
		double d=6.554;
		System.out.println("datatype of d is double and value is "+d);
		String s1="Rudra";
		System.out.println("datatype of s1 is string and value is "+s1);
	}

}
